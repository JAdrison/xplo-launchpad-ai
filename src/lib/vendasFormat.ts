export const formatBRL = (cents: number, masked = false) => {
  if (masked) return "R$ ***";
  return (cents / 100).toLocaleString("pt-BR", {
    style: "currency",
    currency: "BRL",
    minimumFractionDigits: 0,
    maximumFractionDigits: 2,
  });
};

